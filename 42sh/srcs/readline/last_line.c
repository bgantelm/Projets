#include <readline.h>

int	last_line(t_readline *r)
{
	int	j;

	j = (r->i);
	while ((r->line)[j] != '\0' && (r->line)[j] !=
			-2 && (r->line)[j] != -4)
		j++;
	if ((r->line)[j] == '\0')
		return (1);
	return (0);
}
