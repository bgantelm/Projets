#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list *new;

	if ((new = malloc(sizeof(t_list))) == NULL)
		return (NULL);
	if (content == NULL)
	{
		new->content = NULL;
		new->content_size = 0;
	}
	else
	{
		new->content = ft_memalloc(content_size);
		if (new->content != NULL)
		{
			ft_memcpy(new->content, content, content_size);
			new->content_size = content_size;
		}
		else
			new->content_size = 0;
	}
	new->next = NULL;
	return (new);
}
