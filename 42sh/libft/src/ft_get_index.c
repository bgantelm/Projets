#include "libft.h"

int		ft_get_index(char *str, char c)
{
	int			i;

	if (str == NULL)
		return (-1);
	i = 0;
	while (str[i])
	{
		if (str[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
