#include "shell.h"

void		ft_sig_int(int sig)
{
	char	ctrlc[2];

	ctrlc[0] = 5;
	ctrlc[1] = '\0';
	ioctl(0, TIOCSTI, ctrlc);
	sig += 0;
}
